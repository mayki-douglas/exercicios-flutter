class SortingHat {
  static String getPhrase(String house) {
    switch(house) {
      case "Hufflepuff": return 'É fácil rir. É fácil odiar. Mas é preciso coragem para ser gentil.';
      case "Gryffindor": return 'Coragem é acreditar em si mesmo, e isso ninguém pode te ensinar';
      case "Slytherin": return 'Sonserino já nasceu com o maior poder de todos, a superioridade.';
      case "Ravenclaw": return 'Nosso emblema é uma águia, que plana por onde outros não podem escalar';
    }
  }
}